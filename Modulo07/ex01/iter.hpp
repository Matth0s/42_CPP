#ifndef ITER_HPP
# define ITER_HPP

# include <iostream>

template<typename T>
void	iter( T* array, unsigned int len, void(*f)(T&) )
{
	for (unsigned int i = 0; i < len; i++)
		f(array[i]);
};

#endif
